package com.mzeat.model;

public class Advs extends BaseModel {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5424887934791575693L;

	private String id;
	private String name;
	private String img;
	

	

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	
}
